/**
 * Return the banner as a string. Pass a subtitle (e.g. the tagline) to show it
 * beneath the art. Honors NO_COLOR / non-TTY via the color helper.
 */
export function banner(subtitle?: string): string {
  const art = ART.map((l) => c.blue(l)).join("\n");
  const sub = subtitle ? `\n${c.dim(subtitle)}\n` : "\n";
  return `${art}${sub}`;
}
